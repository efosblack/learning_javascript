//switch statements

const grade = 90;

switch(grade){
    case 90:
        console.log('you got an A!');
        break;
    case 75:
        console.log('you got a B!');
        break;
    case 65:
        console.log('you got a C!');
        break;
    case 55:
        console.log('you got a D!');
        break;
    case 50:
        console.log('you got an E!');
        break;
    default:
        console.log('not a valid grade');
}